class Switch
{
    public static void main(String args[])
    {
     char a='A';
     switch(a)
    {
    
    case 'A':
    { 
     System.out.println("Small");
     break;   
    }
    case 'M':
    {
     System.out.println("Medium");
     break;
    }
    
    case 'L':
    {
     System.out.println("Large");
     break;
    }
    case 'E':
    {
     System.out.println("Extra large");
     break;
    }
    default:
    {
     System.out.println("Invalid");
     break;
    }
    
    }
        
    }
}
