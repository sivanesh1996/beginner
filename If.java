class If
{
public static void main(String args[])
    {
     if(5>2)
        {
         System.out.println("5 is greater");
        }
     if(7>2)
        {
         System.out.println("7 is greater");
        }
     if(9<5)
        {
         System.out.println("9 is greater");
        }

     else
        {
         System.out.println("Try Again");
        }

    }
}
